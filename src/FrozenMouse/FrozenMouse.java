    public void freezingMouse(){
        Robot robot;
        Point p;
        try {
            robot = new Robot();
            robot.setAutoDelay(0);
            p = MouseInfo.getPointerInfo().getLocation();
            while(true){
                robot.mouseMove(p.x, p.y);
            }
        } catch (AWTException ex) {
            Logger.getLogger(FrozenMouse.class.getName()).log(Level.SEVERE, null, ex);
        }
}
